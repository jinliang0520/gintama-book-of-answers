// 获取随机答案的函数
function get_random_answer() {
    // 检查答案池是否为空
    if (ANSWER_POOL.length === 0) {
        return "答案池为空，请先添加答案！";
    }
    
    // 生成随机索引
    const randomIndex = Math.floor(Math.random() * ANSWER_POOL.length);
    
    // 返回随机选择的答案
    return ANSWER_POOL[randomIndex];
}



// 页面加载完成后执行
window.addEventListener('DOMContentLoaded', () => {
    // 获取DOM元素
    const questionInput = document.getElementById('question');
    const askBtn = document.getElementById('askBtn');
    const resultDiv = document.getElementById('result');
    
    // 辅助函数：生成银魂标题卡风格的 HTML 
     function format_gintama_style(text) { 
         return ` 
             <div class="gintama-title-card"> 
                 <div class="main-text">${text}</div> 
             </div> 
         `; 
     } 
 
     // 绑定事件部分 
     askBtn.addEventListener('click', () => { 
         const question = questionInput.value.trim(); 
         
         // 如果没有输入问题 
         if (!question) { 
             resultDiv.innerHTML = ` 
                 <div style="text-align:center; color:#8B5A2B;"> 
                     <p>连问题都没有，你是要我不穿内裤出门吗？！</p> 
                 </div>`; 
             return; 
         } 
         
         // 1. 先禁用按钮，防止狂点 
         askBtn.disabled = true; 
         askBtn.textContent = "吵死了..."; 
     
         // 2. 显示随机的吐槽/加载语 
         const randomComplaint = LOADING_COMPLAINTS[Math.floor(Math.random() * LOADING_COMPLAINTS.length)]; 
         resultDiv.innerHTML = `<span style="color: #666; font-style: italic;">${randomComplaint}</span>`; 
     
         // 3. 模拟“不情愿”的延迟 (1.5秒后才出结果) 
         setTimeout(() => { 
             // 加载动画（可选，这里简化处理，直接显示结果） 
             const answer = get_random_answer(); 
              
             // 使用新的样式渲染 
             resultDiv.innerHTML = format_gintama_style(answer); 
              
             // 恢复按钮 - 随机显示三句话中的一句
             const recoveryTexts = ["再二就有再三", "不满意就问到满意为止", "大点声再问一遍"];
             askBtn.disabled = false;
             askBtn.textContent = recoveryTexts[Math.floor(Math.random() * recoveryTexts.length)]; 
         }, 1500); 
     });
});